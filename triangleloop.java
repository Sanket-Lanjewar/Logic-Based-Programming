import java.util.*;
import java.io.*;
public class triangleloop {
    public static void main(String[]args){
       Scanner sc=new Scanner(System.in);
       System.out.println("enter number:");
           int n=sc.nextInt();
            for(int i=0; i<=n;i++){
                for(int j=1; j<=i;j++){
                    System.out.print("*");
                }System.out.println();
            }
        }
    }







    



    